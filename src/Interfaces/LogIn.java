  public boolean Acces(ListaSimple _adminList, String _user, String _password) {
      NodoLS aux = _adminList.getInicio();
        for (int i = 0; i < _adminList.getContador(); i++) {
            if (!_adminList.estaVacia()) {
               if(aux.getUser().getUserr().equals(_user) && aux.getUser().getPassword().equals(_password)){
                   return true;
               }
               aux = aux.getSiguiente();
            }
        }
        return false;
    }
    
    

