    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String adminUser = "ADMIN";
        String adminPassword = "ADMIN";
        if (!"".equals(user.getText()) && !"".equals(password.getText())) {
            if (user.getText().equals(adminUser) && password.getText().equals(adminPassword)) {
                JOptionPane.showMessageDialog(null, "Bienvenido a Amausac");
                Admin a = new Admin();
                a.setResizable(false);
                a.setLocationRelativeTo(null);
                a.setVisible(true);
                this.setVisible(false);

            } else if (Acces(Enterprise.simpleList,user.getText(), password.getText())) {
                String nombre = Enterprise.simpleList.buscarTodo3(user.getText());
                I u = new I();
                u.setBounds(0, 0, 800, 700);
                u.nombre.setText(nombre);
                u.setLocationRelativeTo(null);
                u.setVisible(true);
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, "Usuario o contrase√±a incorrectos");
            }
        } else {
            JOptionPane.showMessageDialog(null, "No puede haber ningun espacio en blanco");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

