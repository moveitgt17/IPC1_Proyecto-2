    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String nombreNuevo = nuevo.getText();
    String userNuevo = user.getText();
    String emailNuevo = email.getText();
    String tarjet = tarjeta.getText();
    String tipoTarjet = (String) combo.getSelectedItem();
    String tipo = "Normal";
    if(!"".equals(tarjet) && !"".equals(tipoTarjet) && !"".equals(nombreNuevo) && !"".equals(userNuevo) && !"".equals(emailNuevo)){
       Usuario usuario = Enterprise.simpleList.busNodo(nombre.getText());
       usuario.setCard(Integer.parseInt(tarjet));
       usuario.setName(nombreNuevo);
       usuario.setEmail(emailNuevo);
       usuario.setUserr(userNuevo);
       usuario.setTipoTarjeta(tipoTarjet);
       Enterprise.simpleList.modificar(usuario);
        JOptionPane.showMessageDialog(null, "Usuario modificado correctamente");
        nombre.setText(nombreNuevo);
    }else{
        JOptionPane.showMessageDialog(null, "No puede haber ningun espacio vacio");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tarjetaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tarjetaKeyTyped
        char validar = evt.getKeyChar();
        if (!Character.isDigit(validar)) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(rootPane, "Ingresar solo numeros");
        }
    
    }//GEN-LAST:event_tarjetaKeyTyped

    private void nuevoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nuevoKeyTyped
        char validar = evt.getKeyChar();
        if (Character.isDigit(validar)) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(rootPane, "Ingresar solo letras");
        }
   
    }//GEN-LAST:event_nuevoKeyTyped

